                                   <h2>Fabric Shields</h2><br />
                                   <p> 
								   
								   Firstly, place the Flange Guard symmetrically on the flanges and fasten it with the velcro. Make sure that the litmus patch is on the outside. </p><br />
								   
                                   <p>There are ropes on the side of the guards. Bring them together & pull them firmly. This will fold the guards cover the nut bolts. Always remember to tie the ropes in a square knot as these wont get loose under pressure. PLease do not pull the rope backward as it might damage the shield</p>                                  
