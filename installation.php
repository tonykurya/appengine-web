                               <div class="txts">
                                   <h2>Stainless Steel Shields</h2><br />
                                   <p>Place the shield on the flange such that the flange is in the center of the shield. Now wrap the guard on the flange and put the double locking latch in one of the slots. Make sure that you choose the slot which gives a snug finish i.e. Neither too tight or loose. Then pull the back locking lever back to lock the guard. It generally takes roughly 20 secs to install a guard.  </p><br />
                                   <p></p>
                               </div>
                               <div class="clear"></div><br />
							   <h2>HDPE Shields</h2><br />
                               <p>Wrap the HDPE flange guard around the flange see to it that the center of the guard is aligned to the center of the flange joint. The fastener head should be facing inside the belt. Make sure all the four orange holders are secured to the flanges. This prevents the guard from slipping off the flanges. Insert the threaded portion of the bolt in any of the given holes and tighten the nut securely. Do not tighten the flange guard very tightly to prevent pressure buildup. During maintenance untighten the nut and take the belt slowly of the bolt. Remove the guard and release all the Orange holders. After completing maintenance put the flange guard back on. </p><br />
                               <p><strong>*No maintenance required for HDPE flange guard</strong></p>
