                                   <p>

Lo que se descubrió por accidente ahora se usa para prevenir accidentes. El doctor Plunkett estaba trabajando en alternativas no tóxicas en refrigerante en DuPont. En uno de sus experimentos, almacenó alrededor de 100 libras de gas TFE-tetrafluoretileno en un cilindro. Después de unos días cuando trató de usar el gas TFE, nada salió del cilindro. Decidió cortar el cilindro con la ayuda de su asistente. Para su sorpresa, el gas TFE se polimerizó en un polvo blanco ceroso. Lo que finalmente se llamó PTFE y fue patentado por DuPont. Actualmente, PTFE es vendido por diferentes fabricantes en varias marcas.</p><br>
                                   <p>
                                       Dale Kleist, un investigador estaba tratando de soldar dos bloques de vidrio para formar un sello hermético. Accidentalmente, el aire comprimido golpeó el vidrio fundido, lo que dio como resultado una forma fácil de crear fibra de vidrio.
                                   </p><br />
                                   <p>
PTFE cuando se recubre sobre tela de fibra de vidrio la convierte en una membrana de alta resistencia a la tracción. PTFE hace la tela térmicamente estable, resistente a los productos químicos, insoluble en casi todos los solventes, resistente a los rayos UV, flexible y no se rompe cuando está sometida a estrés, características de alta resiliencia, coeficientes de fricción más bajos, constante dieléctrica, no inflamable, resistente al agua y excelente aislante. Mientras que Fiberglass mantiene la función estructural de la tela. La fibra de vidrio tiene un alto módulo de elasticidad y alta resistencia a la tracción.</p><br />
                                   <p>Cuidamos al máximo la elección de la tela de las protecciones de brida para que no queden agujeros que eventualmente puedan ocasionar accidentes. El espesor del recubrimiento se verifica para que las protecciones puedan funcionar en condiciones extremas durante períodos prolongados. Usamos hilos de Kevlar que son más fuertes y se descomponen a 800 grados centígrados. Los hilos de Kevlar tienen muy poco estiramiento y son 2-3 veces más fuertes que el nylon o el poliéster. Los hilos Kevlar solo están disponibles en color amarillo y son muy caros.</p><br />
                                   <p>Todos nuestros protectores de brida están hechos de múltiples capas de tela de fibra de vidrio recubierta de PTFE con una malla interior.</p><br />
                                   
