                                   <p>Flanges are used in piping systems commonly for joining pipes, pumps, & valves. Two flanges are bolted with a number of threaded rods & a gasket in between for prevention of a leak. Using flanges in a piping system makes it convenient for maintenance. At times modifications or alterations are required in the flow line and flange connections make it adaptable. Flange connections are governed by number of factors such as flange shapes primarily Blind, Threaded, Slip on, Weld neck, joint type. Each Flange is designed to handle certain amount of pressure and these pressure ratings are defined as per different standards like ANSI -PSI, DIN-PN, JIS-K etc. </p><br />
                                   <p>ANSI is an American standard and PSI is the pressure rating, DIN Is the European standard published by German body Deutsches Institut fur Normung e.V. and PN stands for the pressure rating whereas JIS is the Japanese industrial standard and K stands for the pressure rating. Apart from these we also have British standard and Indian Standard flanges. These standards define the dimensions, physical and chemical properties in a nutshell. Unions are also used in low pressure piping system.</p>                                  
