                               <p><strong>We did a study on the reasons of accidents in Oil and petro chemical facilities. Primary reasons were :</strong></p>
                               <p><strong>1. Storms</strong></p>
                               <p><strong>2. Cable failure</strong></p>
                               <p><strong>3. Piping system leaks primarily flanges.</strong></p><br />
                               <p>Storms are an act of god. These are beyond Human Control. Whereas, most of the cable failures were because of improper maintenance.</p><br />
                               <p>A Number of reasons were attributed to accidents happening because of piping system failures. Primarily these accidents happened during maintenance. What lead to this was "maintenance checks" were not followed. This resulted in flange leaks and sadly accidental deaths of manpower. What is important is these accidents could have been avoided easily. First if proper maintenance checks were followed. Secondly lives could have been saved if<strong> Flange Guard or shields were used on these naked flanges.</strong> </p><br />
                               <p>Flange guard or a flange safety shield is a mechanism through which you safeguard your manpower from chemical, Steam or gas leaks. What is also important to note that these piping systems carry a high pressure, high temperature and toxic fluid or gas. To disperse the pressure a Flange shield is wrapped or tied on these jointing systems. This results in the safe discharge of the toxic liquid and shielding of your manpower and equipment. </p><br />
                               <p>Flange guards come in different MOC's and these safety systems are used as per the process requirement. Choosing the right shield is also very important for the right protection. Not every Flange guard works in every environment. PTFE and SS316 Flange guards are most widely used. Though for a low pressure and low temperature HDPE flange Shields are used which are very cost effective. </p><br />
                               <p>These safety spray shields are widely available in the market though caution should be taken while choosing a manufacturer. Some of the flange guard failures are because of pinholes, Low grade Material, improper design, inadequate coverage by the shield and fraudulent MOC's. It is always recommended to look at the safety angle first as cheap flange spray shields & Pipe spray Shields will limit your purpose and compromise the safety of your manpower and costly equipment. Flange guard is easily available for most of the jointing systems like valves, pumps and flanges. As a buyer you should have your process requirements like the fluid type, design pressure, flange rating, temperature and flange type. This information will help the manufacturer in suggesting you a suitable safety Flange shield. Safety is never secondary it should always be first and a priority. </p>
