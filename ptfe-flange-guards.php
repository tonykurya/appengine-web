                                   <p><B>TIP:</B> Do not tighten the gasket beyond its elastic point to cover lack of symmetry between flanges.<br><br>What was discovered by accident is now used to prevent accidents? Doctor Plunkett was working on non-toxic alternatives on refrigerant in DuPont. In one of his experiments, he stored around 100 pounds of TFE-tetrafluoroethylene gas in a cylinder. After few days when he tried to use the TFE-gas, nothing came out of the cylinder. He decided to cut the cylinder with the help of his assistant. To his surprise, the TFE-gas has polymerized into a waxy white powder. Which was eventually called PTFE and patented by DuPont? Today, PTFE is sold by different manufacturers under several brand names.
</p><br>
                                   <p>
                                      Dale Kleist a researcher was trying to weld to two glass blocks to form an airtight seal. The accidentally molten glass was hit by compressed air which resulted in an easy way of creating fiberglass.
                                   </p><br />
                                   <p>PTFE Coated Fibreglass Fabric - Comprising the coat which is PTFE and the substrate-Fibreglass Fabric adhered together through a coating system.PTFE, when coated on fiberglass fabric, makes it a high tensile membrane. PTFE makes the fabric thermally stable, resistant to chemicals, insoluble in almost all solvents, UV resistant, flexible and does not break when subject to stress, high resilience characteristics, lowest friction coefficients, dielectric constant, nonflammable, waterproof and excellent insulator. Whereas Fibreglass maintains the structural function of the fabric. Fiberglass has high modulus elasticity and high tensile strength.</p><br />
                                   <p>We take utmost care in choosing the fabric for the flange guards so that there are no pin holes which eventually may lead to accidents. Coating thickness is checked so that guard  can perform in extreme conditions for extended periods. We use Kevlar threads which are strongest and decomposes at 800 degrees Fahrenheit or 426 degrees Celsius. Kevlar threads have  very little stretch and are 2-3 times stronger than nylon or polyester. Kevlar threads are only available in Yellow colour and are very expensive.</p><br />
                                   <p>All our flange guards are made of multiple layers of PTFE Coated fiberglass fabric with an inner scrim.</p><br />
                                   
