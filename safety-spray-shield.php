                                   <p>We are putting some figures to understand the extent of damage pipeline & incidents lack of spray safety have caused. In total 4215 incidents have happened resulting in 470 human injuries and property damage of USD 3.4 billion since 2010. This data is based on the cumulative pipeline incidents as per PHMSA report in the US. Out of this more than 1400 incidents have happened alone because of the equipment failure. Other significant reason being improper material,welding corrosion & lack of saftey spray shield. These causes make up almost two third of the reported incidents. </p><br />
                                   <p>Common causes of a flange or joint leak in a pressurised piping system are substandard bolting and gasket material, lack of preventive maintenance, improper alignment of flanges, process line vibrations, thermal deformation and improper bolt tensioning. </p>  <br />
                                   <p>As these piping lines carry service liquids at high pressure, flange leaks are often in form of jet spray & mist formation. This type of spray out causes serious injuries to manpower and damages nearby equipment. It should also be noted that it is operationally very difficult to handle a spray out without a spray shield or a flange guard that diffuses the pressurised splash. When a worker is working in an unsafe environment,the fear of life in the back of his Mind decreases his productivity and overall efficiency. Whereas a damaged equipment may take months to replace and come at a significant cost. </p> <br />
