                                   <p>The Box Type Plastic Flange Guard is extremely efficient in providing protection spray outs & sudden splashes. These PP Box Type Flange Guards have a three fold built-in safety mechanism which completely dissipates the pressure of splash & provides maximum protection to the working staff & expensive equipment.<br><br>
1)	These guards completely cover the flanges from the sides including the stud bolts. This eliminates the risk of any lateral spray out escaping from the guards. This enclosed setup of the PP Box Type Flange Guards also removes any risk of mist formation.<br><br>
2)	The circular design of the box ensures that the splash smoothly moves around the wall of the flange guard. This helps in gradual & effective reduction of pressure & minimizes & risk of spillage.<br><br>
3)	These PP Box Type Flange Guards can contain & collect liquids up to a certain volume. Leakage from flanges joints carrying combustible fluid lines like oil & fuel lines which are directly placed above hot surfaces & electrical equipment. Is a potential fire hazard. These flange Guards contains the splash & prevents it from dripping on these lines.<br><br>
These flange guards are also available with drains which can drain the leak to a safe disposal bin.
