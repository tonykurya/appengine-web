                                   <p>Cloth safety shields are used in Industries for splash protection.Every Industry has a piping system which carries fluids.These fluids types are Oil,acids,alkalis,steam and various hazardous chemicals.These fluids are pressurised and at times heated for process requirements.Your piping system can run from few hundred meteres to few kilometers.The piping system consists of pipes,flanges,valves,pumps and pipe fittings primarily.At any given time there are a large number of workers working around this piping system.Imagine a leak in a flange joint or valve due to gasket failure and the pressurised fluid splashing out on the workers nearby.The temperature and hazardous nature of the fluid makes it more alarming.</p><br>
                                   <p>
