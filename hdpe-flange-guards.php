                                   <p>The creation of HDPE is a mystery in itself. Three patents were filed from 1951 to 1953 by Paul Hogan and Robert banks of Phillips, A.Zletz of Standard Oil and Karl Ziegler of Max Planck Institute. It is interesting to note that Prof. Karl Ziegler had received a Noble Prize for his findings on polymers. After years in court finally patent was granted to Hogan and Banks of Phillips in 1983. The initial production process of Polyethylene required massive pressure from twenty thousand to thirty thousand PSI.A new process was developed using few hundred PSI of pressure by Hogan and Banks which led to the invention of High Density Polyethylene.</p><br />
								   
                                   <p>HDPE is lightweight and very strong. Milk jugs that carry a gallon of milk are made up of just two ounces of HDPE. Even car manufacturers make Fuel Tanks of HDPE resulting in increase fuel efficiency. It is an essential product used in manufacturing by medical and health industries. HDPE meets USDA and FDA certification of having properties of being non-toxic and non staining and can be used in food processing. Typical properties of HDPE have good moisture and impact resistance, high resistance to chemicals, flexible, UV resistance and high tensile strength. For this reason, we use HDPE To manufacture high qality flange guards for safety from spray outs. </p>  <br />
								   
