                                   <h5>Understanding Flanges,gaskets and leaks</h5>
                                   <p>Billions workers across the world are exposed to harmful risks at their workplaces Most of these workers come from developing Countries. India Accounts for almost 20% of HSE cases. Five states account for more than 50 percent of the total factories in India. Organized sector only accounts for 16 percent of total work force.</p><br>
                                   <p>
                                       <strong>Flanges </strong></p>
                                   <br><p>Flanges are an integral part of a piping system. They are used for joining pipes, valves and pumps.</p><br>
								   <p><b>Types of Flanges</b><br><br>
1.Weld Neck Flange<br>
2.Lap Joint Flange<br> 
3.Socket Welding Flange<br>
4.Slip on Flange<br>
5.Blind Flange<br>
6.Orfice Flange<br>
7.Threaded Flange<br>
8.Reducing Flange<br>
9.Stub End Flange<br>

									   <br><b>Flange Standards</b><br><br>

1.ASME B16.5 or ASME B16.47 - The American Society of Mechanical Engineers <br>
2.DIN EN 1092-1 – European Standard <br>
3.API Spec 6A - Surface drilling and Production<br>
4.MSS SP-44 - Surface drilling and Production<br>
5.JIS B 2220:2004 -Japanese Standard for Steel Pipe Flanges<br>
6.BS 10 : 1962 – British Standard for Flanges and Bolting</p>



                               </div>
                               <div class="clear"></div>
							   <br><p><b>Gaskets</b></p><br>
                               <p> Gaskets is used to seal the spacing between two faces of connecting flanges. The bolt load compresses the gasket to create a sufficient barrier at the pressurized side of the gasket. Thus blocking the fluid leakage. The problem is flange faces are subject to surface damage and there are numerous reasons. Flange faces are often subject to surface damage by wire brush, screw drivers, chisel etc. A damaged area may results in leakage as sealing capability of the gasket is compromised. The best practice is to always protect flange faces from any form of damage.</p><br />
							   
							   <p> <b>Common Causes of Gasket Leaks</b> <br><br>

1.	Excessive bolting pressure <br>
2.	Improper seating or centering of gasket<br>
3.	High vibrations<br>
4.	High temperature fluctuation<br>
5.	Improper gasket material<br> <p> <br />
							   
                               <p><b>Flange leak checking Methodology</b><br><br>

Two widely used methods are practiced in prevalent Process Industry. <br><br>

•	Pressure Equivalent Method<br>


•	ASME BPVC Sec VIII Div 1 Appendix 2 method<br>

.<p><br/>
                               <p><b>Flange Leakage Common Reasons:</b><br>

•	Uneven tightening of Bolts and Studs – Improper tightening of bolt without following procedure leads to uneven stress. This results in damage to the gasket and leakage.<br>
•	Alignment -In simple word improper adjustment of the flanges which results in a flange seating gap.<br>
•	Centering of Gaskets -The positioning of gasket around the flanges is very important for creation of the flange leak barrier.<br>
•	Flange material – Flange raw material should be considered carefully in consideration with the minimum or maximum process temperature. As sub standard material can lead to bending of flanges.<br>
•	Temperature and Pressure – Every flange has been marked for a certain pressure and temperature requirement. Fasteners should be in accordance. Any improper deviation will lead to bolt failure.<br>
•	Maintenance – Flanges are connected to pipe lines which carry fluids. These pipelines are subject to vibrations. Vibrations lead to loosening of fasteners. Loosening of fasteners leads to flange leakage. So in all cases proper maintenance schedule should be followed.<br>
•	Flange Surface -Flange Surface should be even and carefully monitored for scratches, distortion and cracking. This is one of the most common cause of a flange leakage. <br>

<p><br />
                               
                               <p>
                                   Maintenance leads to preventions of unwanted damage to equipment and leakage. This is essential for proper running of the plant without breakdowns and work place accidents.</p><br>

<p><strong> To prevent accidents from fluid spray outs  due to flange leakage, install flange guards. <br>
	
	Piping system carrying hazardous chemicals at high temperature and pressure have a tendency to spray outs its harmful contents via compromised flange joints. These leaking flanges injure the working staff and damages expensive equipments kept nearby. <a href="p-guards.php">Flange Guards</a> installed on a joint diffuses the pressure of the spray out and prevents it from comming in contact with any working staff or equipments. These are also called <a href=""> Flange Spray Guards</a> or Flange Safety Shields.
								   </strong> </p>
                               
