                                   <div class="txts1">
                                       <h5><strong> Step 1:</strong>  Determine The Type Of Liquid Flowing Through Your Pipes:</h5>
                                       <p><span>1.</span> Acids, caustics, solvents, fuel, lubrication</p>
                                       <p><span>2.</span> Water-based liquids and low-con chemicals like Medical Industries</p>
                                       <p><span>3.</span>Highly Concentrated Acids, seal & hydraulic oil, steam and other chemicals</p><br />
                                       <p>For Type A, Flange Guards Made From PTFE Material Can Provide The Maximum Safety. For Type B, Using PP & HDPE Flange Guards Is Recommended As The Liquid Is Not Very Harsh. For Type C, S316Flange Guards Can Be Used.  This Classification Is Subject To The Temperature & Pressure As Mentioned In Step 2.</p>

                                   </div>
                               </div>
                           <div class="imgtxt">
                               <div class="txts1">
                                   <h5><strong> Step 2:</strong>  Determine The Temperature</h5>
                                   <p> Different Shield Materials Can Support Different Temperature. Subject To The Above Segmentation, The Following Is The Temperature Range Of Support For Each Flange Guard Material</p>
                                   <p><span>1.</span> PP – HDPE FLANGE GUARD : <strong>Upto 90 °C</strong></p>
                                   <p><span>2.</span> PTFE FLANGE GUARDS: <strong>Upto 240 °C</strong></p>
                                   <p><span>3.</span> SS316 Flange Guards: <strong>Upto 600 °C</strong></p><br />
                                   <p><span>4.</span> Other SS Flange Guards: <strong>600 °C & Above</strong></p>
                               </div>
                           </div>
                           <div class="imgtxt">
                               <div class="txts1">
                                   <h5><strong> Step 3:</strong>  Determine The Pressure:</h5>
                                   <p>Pressure Also Plays A Important Factor While Deciding Upon A Flange Guard Material. Material Should Be Capable Enough To Handle The Pressure Of The Spray Outs.</p>
                                   <p><span>1.</span> PP-HDPE Flange Guards:<strong>Upto 300 PSI</strong> </p>
                                   <p><span>2.</span> PTFE FLANGE GUARDS <strong>Upto 900 PSI</strong></p>
                                   <p><span>3.</span> SS Flange Guards:  <strong>Upto 2000 PSI</strong></p><br />
                                   <p>However If You Need Flange Guards Of A Particular Shield Material For A Higher Value Of Pressure Support, We Can Help You Out With The Same.</p><br />
                                   <p>All The 3 Variable Have To Be Taken Into Account While Choosing The Shield Material. For Example: If You Want To Use The Flange Guard For Water Based Solutions, PP & HDPE Flange Guards Would Be Sufficient For You Till 90 °C. But If The Temperature Is Say 225 °C, You Will Have To Opt For PTFE Flange Guard. Now In The Case If The Pressure Is Above 900 PSI, You Will Have To Use SS316 Flange Guards.</p><br />
                                   <p>Below We Will Discuss A Bit About Each Shield Material & The Type Of Flange Guards Made From It.</p>
                               </div>
                           </div>
                           <div class="imgtxt">
                               <div class="txts1">
                                   <h5><strong>PTFE FLANGE GUARDS :</strong></h5>
                                   <p>PTFE Flange Guards are made from multiple layers of PTFE coated fiberglass fabric. Layers of PTFE Fiberglass clothes are sewed together with the help of military grade Kevlar threads or PTFE Threads with a central scrim. The locking mechanism for PTFE Flange Guards involve industrial Grade Velcro & Heal Resistant Tie Strings</p>
                               </div>
                           </div>
                           <div class="imgtxt">
                               <div class="txts1">
                                   <h5><strong>PP- HDPE Flange Guard :</strong></h5>
                                   <p>HDPE – PP Flange Guards are made from Sheets of HDPE  or PP. These Sheets are automatically cut with the help of machines.  The locking mechanisms involve Nut Bolt & Strap locking with spacers across the surface of flanges.</p>
                               </div>
                           </div>
                           <div class="imgtxt">
                               <div class="txts1">
                                   <h5><strong>SS316 Flange Guards :</strong></h5>
                                   <p>SS316 Flange Guards are made from thick sheets. These sheets are then reinforced with layers of Wired Mesh to diffuse the pressure. The locking mechanism of these flange guards is double locking latch which can be quickly installed & uninstalled.</p>
