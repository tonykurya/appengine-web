                                   <p>
Il PTFE rivestito su tessuto in fibra di vetro lo rende una membrana ad alta resistenza. Il PTFE rende il tessuto termicamente stabile, resistente agli agenti chimici, insolubile in quasi tutti i solventi, resistente ai raggi UV, flessibile e non si rompe quando soggetto a stress, elevate caratteristiche di resilienza, coefficienti di attrito più bassi, costante dielettrica, non infiammabile, impermeabile ed eccellente isolante. Considerando che la vetroresina mantiene la funzione strutturale del tessuto. La fibra di vetro ha un alto modulo elastico e un'elevata resistenza alla trazione.</p><br />
                                   <p> Ci prendiamo la massima cura nella scelta del tessuto delle protezioni flangiate in modo che non ci siano fori che potrebbero causare incidenti. Lo spessore del rivestimento viene controllato in modo che le protezioni possano lavorare in condizioni estreme per lunghi periodi. Usiamo fili di Kevlar che sono più forti e si decompongono a 800 gradi Celsius. I fili in kevlar hanno uno stiramento molto ridotto e sono 2-3 volte più forti del nylon o del poliestere. I fili di Kevlar sono disponibili solo in giallo e sono molto costosi. </ P> <br />
