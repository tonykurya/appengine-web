<P>
Stiamo mettendo alcune cifre per comprendere l'entità del danno causato da condotte e incidenti. Sono stati segnalati un totale di 4215 incidenti che hanno provocato 470 danni umani e danni alla proprietà di 3,4 miliardi di dollari dal 2010. Queste cifre si basano su incidenti cumulativi di gasdotti secondo il rapporto PHMSA degli Stati Uniti. Di questi, oltre 1400 incidenti si sono verificati da soli a causa di guasti alle apparecchiature. Un altro motivo significativo è costituito da materiale improprio, corrosione da saldatura e mancanza di copriflangia di sicurezza. Queste cause costituiscono quasi i due terzi degli incidenti segnalati. </P> <br>
                                   <P>
                                       Cause comuni di una flangia o fuga congiunta in un sistema di tubazioni pressurizzato sono bulloni e materiale di guarnizione scadenti, mancanza di manutenzione preventiva, allineamento scorretto delle flange, vibrazioni della linea di processo, deformazione termica e tensionamento errato dei bulloni.
                                   </P> <br />
                                   <p>
Poiché queste tubazioni trasportano liquidi di servizio ad alta pressione, le perdite della flangia sono spesso sotto forma di getto d'acqua e formazione di nebbia. Questo tipo di spray causa gravi danni alla manodopera e danneggia le apparecchiature vicine. Va anche notato che è molto difficile da gestire a livello operativo uno spray senza copriflangia di sicurezza che diffonda lo splash pressurizzato. Quando un lavoratore lavora in un ambiente non sicuro, la paura della vita nella parte posteriore della sua mente diminuisce la sua produttività e l'efficienza complessiva. Considerando che un equipaggiamento danneggiato può richiedere mesi per sostituire e comportare un costo significativo</p><br />

                                   <p> Quando si prevede di acquistare uno scudo di sicurezza, il punto più importante da considerare è se i copriflangia di sicurezza serviranno al suo scopo. copriflangia di sicurezza o Flange Guards possono venire da te a un costo molto basso ma senza una funzionalità adeguata, è solo un "COSTO".</p> <br />
                                   <p>  Copriflange di sicurezza are available in various materials. Two broad types of flange guards are Copriflange di sicurezza tessili & Copriflange di sicurezza metallici. Copriflange di sicurezza tessili is made from PTFE Coated Fiber Glass Cloth, virgin ptfe, PVC, PPL where as Copriflange di sicurezza  metallici is made from SS 316 only. </ p> <br />
                                   
