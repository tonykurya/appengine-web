                                   <h5>Stainless steel grade-316 flange Guards</h5>
                                   <p>Successful Stainless steel creation goes back to 1913 when Harry Brearley created a steel with 12.8% chromium and a carbon percentage of merely 24 percent. Stainless steel was made with the sole purpose of creating a weather and acid resistant steel. One peculiar characteristic of Stainless steel is it cannot be hardened by heating. Adding Molybdenum to steel increases its corrosion resistance, heat resistance and overall strength. Stainless steel 316 is a Chromium-Nickel-Molybdenum alloy..</p><br>
                                   <p>
                                       Flanges are joiners or connectors used in connecting pipes and equipment manually.</p>
                               </div>
                               <div class="clear"></div>
                               <p>
                               <strong>These flanges are bolted together by a torque wrench. Though it sounds simple but is very critical. Critical because of dangerous, high temperature and volatile liquids continuously passing through them. Flanges are present everywhere from nuclear-powered submarines to your neighbourhood water pipelines. There are many reasons for a flange leak like the improper alignment of flanges, loosening of bolts, improper gasket material, vibrations, lack of maintenance etc.
